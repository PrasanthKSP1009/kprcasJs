//Relational operator >, <, <=, >=, ===, !==  
//output - true/false
a = 10
b = 9
console.log("Relational operator <", a<b);
console.log("Relational operator >", a>b);
console.log("Relational operator <=", a<=b);
console.log("Relational operator >=", a>=b);
console.log("Relational operator ===", a===b);
console.log("Relational operator !==", a!==b);

